#include "ft_printf.h"

void	ft_putstr(char *str, int *len)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		write (1, "(null)", 6);
		*len += 6;
		return ;
	}
	while (str[i])
	{
		*len += ft_putchar(str[i]);
		i++;
	}
}
